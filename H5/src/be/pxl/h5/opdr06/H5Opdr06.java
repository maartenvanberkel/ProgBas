package be.pxl.h5.opdr06;

public class H5Opdr06 {

	public static void main(String[] args) {
		int number1 = 34;
		int number2 = 45;
		System.out.println("number1: " + number1);
		System.out.println("number2: " + number2);
		System.out.println("number1 < number2: " + (number1 < number2));
		System.out.println("number1 <= number2: " + (number1 <= number2));
		System.out.println("number1 > number2: " + (number1 > number2));
		System.out.println("number1 >= number2: " + (number1 > number2));
		System.out.println("number1 == number2: " + (number1 == number2));
		System.out.println("number1 != number2: " + (number1 != number2));
		
		
		
	}
}